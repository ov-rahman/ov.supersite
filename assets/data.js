function bsArt(iconEmoji, c1, c2, label) {
  return `
  <svg viewBox="0 0 400 200" preserveAspectRatio="xMidYMid slice" xmlns="http://www.w3.org/2000/svg">
    <defs>
      <linearGradient id="g${c1.slice(1)}${c2.slice(1)}" x1="0" y1="0" x2="1" y2="1">
        <stop offset="0" stop-color="${c1}"/><stop offset="1" stop-color="${c2}"/>
      </linearGradient>
    </defs>
    <rect width="400" height="200" fill="url(#g${c1.slice(1)}${c2.slice(1)})"/>
    <g opacity="0.18" transform="translate(200,100)">
      ${Array.from({length:12},(_,i)=>`<path d="M0 0 L${Math.cos(i*Math.PI/6)*300} ${Math.sin(i*Math.PI/6)*300} L${Math.cos((i*Math.PI/6)+0.16)*300} ${Math.sin((i*Math.PI/6)+0.16)*300} Z" fill="#fff"/>`).join('')}
    </g>
    <text x="40" y="70" font-size="34" opacity="0.75" transform="rotate(-15 40 70)">⭐</text>
    <text x="340" y="160" font-size="28" opacity="0.7" transform="rotate(12 340 160)">⭐</text>
    <text x="320" y="55" font-size="20" opacity="0.6" transform="rotate(20 320 55)">✨</text>
    <text x="60" y="165" font-size="20" opacity="0.6" transform="rotate(-18 60 165)">✨</text>
    <text x="200" y="118" font-size="86" text-anchor="middle">${iconEmoji}</text>
    <text x="200" y="185" font-size="20" text-anchor="middle" fill="#fff" opacity="0.95"
      font-family="'Lilita One',sans-serif" stroke="#0c0f38" stroke-width="4" paint-order="stroke"
      style="letter-spacing:1px">${label}</text>
  </svg>`;
}

